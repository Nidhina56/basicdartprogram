void main()
{
  int a = 5;
  int b = 9;
  var c = a > b;
  print("a is greater than b:${c}");
  print("-------------------------");
  var d = a < b;
  print("a is smaller than b:${d}");
  print("-------------------------");
  var e = a >= b;
  print("a is greater than b:${e}");
  print("-------------------------");
  var f = a <= b;
  print("a is smaller than b:${f}");
  print("-------------------------");
  var g = b == a;
  print("a and b are equal:${g}");
  print("-------------------------");
  var h = b != a;
  print("not equal:${h}");
}
